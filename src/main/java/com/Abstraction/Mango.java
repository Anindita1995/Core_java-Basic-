package com.Abstraction;

public class Mango extends Fruit{

	
	@Override
	void taste() {
		System.out.println("Mango tastes sweet");
		
	}

	@Override
	void flower() {
		System.out.println("Mango tree has flowers");		
	}

	@Override
	void branches() {
		System.out.println("Mango tree has branches");
		
	}

}
